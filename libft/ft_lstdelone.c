#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	t_list	*maillon;

	maillon = *alst;
	del(maillon->content, maillon->content_size);
	free(*alst);
	*alst = NULL;
}
