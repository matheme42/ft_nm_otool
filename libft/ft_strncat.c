#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t k;

	i = 0;
	k = 0;
	while (s1[k])
		k++;
	while (s2[i] && i < n)
	{
		s1[k + i] = s2[i];
		i++;
	}
	s1[k + i] = '\0';
	return (s1);
}
